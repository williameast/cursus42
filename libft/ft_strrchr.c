#include <stdlib.h>
#include "libft.h"

char	*ft_strrchr(const char *str, int c)
{
	int	i;

	i = ft_strlen(str);
	while (str[i] >= 0 && *str != c)
		str--;
	if (str[i] == c)
		return ((char *)str);
	return (0);
}
